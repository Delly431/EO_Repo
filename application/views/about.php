<h1>Exiled Order History</h1><br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;October 2014 to February 2015:
Exiled Order was established in October of 2014, and was known as an ArmA 3 community. We were known as a professional Mil-Sim community that completed online operations suitable for 50 players at a time, along with having 
a re-vamped Invade and Annex public server. After a few different expansion options we decided to put ArmA 3 on the back burners, due to some Administration and server issues. At this time we quickly realized that we wanted 
to create a community that something even bigger than what we had envisioned when we first established [EO]. 

<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;January to December 2015:
During this time Exiled Order had played in two season of CS:GO and realised that this title would bring more members, along with the potential of moving higher in the gaming ladder. Exiled Orderâ€™s Research and Development 
section was on a mission to find the next big thing, and we quickly came to the conclusion that it was SQUAD. As SQUAD was being developed we continued to observe and follow the updates, we then knew that it was going to 
be a big break for us. Counter-Strike: Global Offensive, War Thunder, and World of Warcraft were on our minds, and we have continue to help support and grow those games with the help of our members. 

Now that SQUAD has come to steam and Exiled Order has picked up a server we have grown at a rate we could not have even imagined. With the community growing everyday we have had to open multiple squads to accommodate 
the growing numbers. What the Administration and Staff at Exiled Order has envisioned in 2014 has slowly and will continue to grow over the coming years. 
<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;January to June 2016:
With only half of this year completed, Exiled Order has continued to support Squad, Counter-Strike: Global Offensive, War Thunder and World of Warcraft. Now that Squad has been out for awhile on steam for early access, 
we have also made World of Warcraft: Legion our second main focus. 
As the year continues, we will continue to update the progression and history of Exiled Order... Check back later for more updates.

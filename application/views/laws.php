<h1>Cheating</h1>
Neither associate nor member shall use/promote hacks, exploits, bots, or illegal third party programs. Any activity that violates a game's EULA*/ToS**/CoC***/NDA**** is prohibited. Any bans made by third party anti-cheat organizations (such as PB/Vac/GUID bans for hacking) will result in a permanent ban that can only be overturned by the third-party.<br /><br />
 
*EULA - End User License Agreement<br />
**ToS - Terms of Service<br />
***CoC - Code of Conduct<br />
****NDA - Non-disclosure Agreement <br />   
<br /><br />
This will end in the result of a “Severe Offense” and those who are caught will be permanently banned from all Exiled Order assets.  
<br /><br />
<h1>Public Behavior (Intended for members and guests)</h1><br />
<h2>Forums:</h2>
<ul>
<li>
All affiliates and members are expected to treat others with respect and refrain from behavior deemed hurtful to others or damaging to the integrity of Exiled Order. Members should conduct themselves appropriately on forums and in chats 
within and outside of the community.
</li>
<li>
Trolling, malicious comments, or posting inappropriate content is not allowed.
</li>
<li>
No sexual advances, harassment, or pornographic material regardless of age.
</li>
<li>
Forum spam (A post/thread that contains irrelevant or useless information or is off-topic from the original post/forum) will not be tolerated.
</li> 
<li>
Swearing on the forums is allowed so long as it is not directed at another member or affiliate and it is not excessive. Swearing is not allowed in thread titles.
</li>
<li>
Racism is not allowed, failure to comply will result in a suspension. This is your only warning.
</li>
</ul>
<br />
<h2>Teamspeak: </h2>
<ul>
<li>
All actions on Team Speak 3 must be in a professional manner.
</li>
<li>
Racism is not allowed, failure to comply will result in a suspension/ban.
</li>
<li>
This is your only warning.
</li>
<li>
Channel spamming will not be tolerated. This is when you switch channels at a rapid rate.
</li>
</ul>
<br />
<h2>Game Server / In-Game:</h2>
<ul>
<li>
All actions in game must be in a professional manner.
</li>
<li>
Team-killing is not allowed, unless deemed accidental by both parties involved.
</li>
<li>
Attacking an uncap/main/base will result in a ban. We do not tolerate this and will be considered a permanent ban, you may be able to appeal this.
</li>
<li>
Racism is not allowed, failure to comply will result in a suspension/ban. This is your only warning.
</li>
<li>
Respect all players on our servers, Admins get the final say.
</li>
<li>
Excessive team swapping will not be tolerated.
</li>
<li>
Promotion of other clans/communities on our server(s) will not be tolerated. i.e: Squad name being xxxxx.com
</li>
</ul>
<br />
<h1>Membership & Accountability</h1>
<ul>
<li>
In order to be a member of Exiled Order all new prospects must select a primary game section that is supported by the community, when they are in the application process.
</li>
<li>
All membership is required to at least put in the amount of time that they have stated on their application and interview. This includes Game Server(s), Teamspeak 3 Server and Forums. 
Members are requested to check and be active on our forums as much as possible.
</li>
<li>
All gaming handles are to be consistent with the Forums, Teamspeak and in-game.
</li>
<li>
If a member is playing a game that Exiled Order officially/unofficially supports, we ask that you represent our community.
</li>
<li>
Do NOT recruit on any other community game servers, voice server and/or forums. If someone comments towards our community, message them privately.
</li>
<li>
Multi-Clanning is NOT allowed. Violators will be issued a permanent ban if caught. This excludes World of Warcraft and Guild Wars 2.
</li>
<li>
All Game Section, Operations and Legal department members are required to follow the set forth Chain of Command. (Refer to the Ranks page of the website.)
</li>
</ul>
